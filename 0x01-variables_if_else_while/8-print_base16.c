#include <stdio.h>

/**
 * main - a C program that prints all the numbers of hexadecimal in lowercase
 *
 * Return: Always 0
 */

int main(void)
{
{
int i = 48;
while (i < 58)
{
putchar(i);
i++;
}
}
{
int i = 97;
while (i < 103)
{
putchar(i);
i++;
}
}
putchar('\n');
return (0);
}
